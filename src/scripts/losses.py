class LovaszHingeLoss(nn.Module):
    """
        This complex loss function was introduced by Berman, Triki and Blaschko in their paper "The Lovasz-Softmax loss: A tractable surrogate for the optimization of the intersection-over-union measure in neural networks", 
    retrievable here: https://arxiv.org/abs/1705.08790. It is designed to optimise the Intersection over Union score for semantic segmentation, particularly for multi-class instances. 
    Specifically, it sorts predictions by their error before calculating cumulatively how each error affects the IoU score. 
    This gradient vector is then multiplied with the initial error vector to penalise most strongly the predictions that decreased the IoU score the most.
    """
    def __init__(self, weight=None, size_average=True):
        super(LovaszHingeLoss, self).__init__()

    def forward(self, inputs, targets):
        inputs = F.sigmoid(inputs)    
        Lovasz = lovasz_hinge(inputs, targets, per_image=False)                       
        return Lovasz
    

